
// Video games exist as content that has ownership. The ownership belongs to the 
// entitys that make a Claim to owning the game, supported by Evidence.
public class videoGame {
	String name;
	String publishingParty;
	public videoGame(String name, String publishingParty){
		this.name = name;
		this.publishingParty = publishingParty;
	}
	
}
